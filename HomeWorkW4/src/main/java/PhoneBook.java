import java.util.HashMap;
import java.util.Scanner;

public class PhoneBook {


    public static void main(String[] args) {
        /* Create a phonebook using hashmap to store Name and Number

         */
        HashMap< String, String > PhoneBook = new HashMap<String, String>();
        PhoneBook.put("Henry", "3333333333");
        PhoneBook.put("Nyshia", "3334443434");
        PhoneBook.put("Mike", "2813308004");
        PhoneBook.put("Amy", "1234567890");
        System.out.println("");

    }


}
